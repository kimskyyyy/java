package conditions;

public class Ex01 {

	public static void main(String[] args) {
		
		/*
		 * if(조건식) {
		 *	 조건식의 최종 결과가 true일 때 실행할 문장 배치
		 * }
		 * 
		 * 조건식은 true/false만 가능(정수, 실수, 문자열 불가)
		 */
		
		if(true) {
			System.out.println("조건식이 참일 때 실행할 문장");
		}
		
		if(false) {
			System.out.println("조건식이 거짓일 때 실행할 문장이지만 실행 될 수 없음");
		}
	}

}
